export const getShopItems = (rank) => {
  // Convert rank letter to numerical value for comparison
  const rankValue = {
    'E': 1,
    'D': 2,
    'C': 3,
    'B': 4,
    'A': 5,
    'S': 6
  }[rank];

  return {
    weapons: items.weapons.filter(item => rankValue >= rankValue[item.rank]),
    armor: items.armor.filter(item => rankValue >= rankValue[item.rank]),
    potions: items.potions.filter(item => rankValue >= rankValue[item.rank])
  };
};
